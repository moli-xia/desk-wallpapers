    def set_window_icon(self, window):
        """为窗口设置图标"""
        try:
            # 首先尝试使用resource_path查找图标（适用于打包后的exe）
            icon_path = resource_path("app_icon.ico")
            if os.path.exists(icon_path):
                try:
                    # 尝试使用iconbitmap方法
                    window.iconbitmap(icon_path)
                    print(f"成功设置图标(iconbitmap): {icon_path}")
                    return
                except Exception as e:
                    print(f"iconbitmap失败: {e}，尝试iconphoto方法")
                    # 如果iconbitmap失败，尝试使用iconphoto
                    from PIL import Image, ImageTk
                    img = Image.open(icon_path)
                    photo = ImageTk.PhotoImage(img)
                    window.iconphoto(True, photo)
                    window._icon_photo = photo  # 保存引用避免被垃圾回收
                    print(f"成功设置图标(iconphoto): {icon_path}")
                    return
            
            # 然后尝试使用相对路径app_icon.ico（适用于开发环境）
            if os.path.exists("app_icon.ico"):
                try:
                    window.iconbitmap("app_icon.ico")
                    print("成功设置图标(iconbitmap): app_icon.ico")
                    return
                except Exception as e:
                    print(f"iconbitmap失败: {e}，尝试iconphoto方法")
                    from PIL import Image, ImageTk
                    img = Image.open("app_icon.ico")
                    photo = ImageTk.PhotoImage(img)
                    window.iconphoto(True, photo)
                    window._icon_photo = photo
                    print("成功设置图标(iconphoto): app_icon.ico")
                    return
                
            # 如果ico文件不存在，创建一个简单的备用图标
            print("未找到app_icon.ico文件，使用备用图标")
            from PIL import Image, ImageTk
            img = Image.new('RGBA', (32, 32), (74, 144, 226, 255))
            from PIL import ImageDraw
            draw = ImageDraw.Draw(img)
            draw.ellipse([4, 4, 28, 28], fill=(255, 255, 255, 200))
            draw.text((10, 12), "WP", fill=(74, 144, 226, 255))
            
            # 转换为PhotoImage并设置
            photo = ImageTk.PhotoImage(img)
            window.iconphoto(True, photo)
            window._icon_photo = photo
            print("使用备用图标")
                
        except Exception as e:
            print(f"设置窗口图标失败: {e}")

