    def is_startup_enabled(self):
        """检查是否已启用开机启动"""
        try:
            import winreg
            key = winreg.OpenKey(winreg.HKEY_CURRENT_USER, 
                               "Software\\Microsoft\\Windows\\CurrentVersion\\Run", 
                               0, winreg.KEY_READ)
            try:
                value, _ = winreg.QueryValueEx(key, "WallpaperDownloader")
                winreg.CloseKey(key)
                return True
            except FileNotFoundError:
                winreg.CloseKey(key)
                return False
        except Exception as e:
            print(f"检查开机启动状态失败: {e}")
            return False

    def set_startup(self, enable):
        """设置或取消开机启动"""
        try:
            import winreg
            key = winreg.OpenKey(winreg.HKEY_CURRENT_USER, 
                               "Software\\Microsoft\\Windows\\CurrentVersion\\Run", 
                               0, winreg.KEY_SET_VALUE)
            
            if enable:
                # 获取当前程序的完整路径
                if getattr(sys, 'frozen', False):
                    # 如果是打包的exe文件
                    app_path = sys.executable
                else:
                    # 如果是Python脚本，使用python解释器运行
                    app_path = f'"{sys.executable}" "{os.path.abspath(__file__)}"'
                
                winreg.SetValueEx(key, "WallpaperDownloader", 0, winreg.REG_SZ, app_path)
                print(f"已添加开机启动: {app_path}")
            else:
                try:
                    winreg.DeleteValue(key, "WallpaperDownloader")
                    print("已取消开机启动")
                except FileNotFoundError:
                    print("开机启动项不存在")
            
            winreg.CloseKey(key)
            return True
            
        except Exception as e:
            print(f"设置开机启动失败: {e}")
            messagebox.showerror("错误", f"设置开机启动失败: {e}")
            return False

